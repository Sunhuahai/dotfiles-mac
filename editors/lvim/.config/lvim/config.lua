-- 开启自动换行
vim.opt.wrap = true

-- 优化换行体验：不在单词中间断开，而是保持单词完整
vim.opt.linebreak = true

-- 视觉上的缩进：换行后的行首会与上一行对齐，看起来更整齐
vim.opt.breakindent = true

